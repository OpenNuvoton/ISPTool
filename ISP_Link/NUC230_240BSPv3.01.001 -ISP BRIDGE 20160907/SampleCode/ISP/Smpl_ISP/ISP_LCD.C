void ILI9341_LCD_SetAddress(unsigned int x1,unsigned int x2,unsigned int y1,unsigned int y2)
{
	    if(x1 >= 240)
        x1 = 239;
    if(x2 >= 240)
        x2 = 239;  
    if(y1 >= 320)
        y1 = 319;  
    if(y2 >= 320)
        y2 = 319;  
	write_command(0x2a); 	//x?   
  write_data(x1>>8);
	write_data(x1);
  write_data(x2>>8);
	write_data(x2);
	write_command(0x2b); 	//y?   
	write_data(y1>>8);
	write_data(y1);
  write_data(y2>>8);
	write_data(y2);
  write_command(0x2c);
	
}


void ILI9341_LCD_PutChar8x16(uint16_t x, uint16_t y, uint8_t c, uint32_t fColor, uint32_t bColor)
{
	uint32_t i,j;
	for(i=0;i<16;i++){
        uint8_t m=Font8x16[c*16+i];
        ILI9341_LCD_SetAddress(x+i,x+i,y,y+7);
        write_command(0x2c);        
        
		for(j=0;j<8;j++){
			if((m&0x01)==0x01){
                write_data(fColor>>8);
				write_data(fColor);
			}
			else{
        write_data(bColor>>8);
				write_data(bColor);
			}
			m>>=1;
		}
	}
}


void ILI9341_LCD_PutString(uint16_t x, uint16_t y,uint8_t *s, uint32_t fColor, uint32_t bColor)
{
    uint8_t l=0;
    while(*s){
        if(*s<0x80){
            ILI9341_LCD_PutChar8x16(x,312-y-l*8,*s,fColor,bColor);
            s++;
            l++;
		}
	}	
}

void ILI9341_LCD_PutString_line(uint16_t x, uint16_t y,uint8_t *s, uint32_t fColor, uint32_t bColor)
{
    uint8_t l=0;
    while(*s){
        if(*s<0x80){
            ILI9341_LCD_PutChar8x16((x*16),312-y-l*8,*s,fColor,bColor);
            s++;
            l++;
		}
	}	
}
