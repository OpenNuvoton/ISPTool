void dsp_single_color(unsigned int LCD_DATA)
{
	unsigned int i,j;
	Address_set(0,240-1,0,320-1);
	 write_command(0x3c);         
	for(i=0;i<240;i++){
		for(j=0;j<320;j++){
			write_data(LCD_DATA>>8);
			write_data(LCD_DATA);
    }
	}	
}


void LCD_Fill(unsigned int x_start, unsigned int y_start,unsigned int x_end, unsigned int y_end,  unsigned int LCD_DATA)
{
	unsigned int n;
	Address_set(y_start,y_end,x_start,x_end);
	write_command(0x3c);        
	n=(y_end-y_start+1)*(x_end-x_start+1);  
	while(n--)
	{
	write_data(LCD_DATA>>8);
			write_data(LCD_DATA);	
	}
	Address_set(0,240-1,0,320-1);
}


