std::string CPartNum::GetPartNumber(unsigned int uID, unsigned int uCoreType, unsigned int *puProjectCode) const
{
    int i;

    if (uCoreType == 0)
    {
        for (i = 0; i < _countof(g_PartNumIDs); ++i)
        {
            if (g_PartNumIDs[i].uID == uID)
            {
                if (puProjectCode != NULL)
                    *puProjectCode =  g_PartNumIDs[i].uProjectCode;

                return g_PartNumIDs[i].szPartNumber;
            }
        }
    }
    else
    {
        for (i = 0; i < _countof(g_PartNumIDs_51); ++i)
        {
            if (g_PartNumIDs_51[i].uID == uID)
            {
                if (puProjectCode != NULL)
                    *puProjectCode =  g_PartNumIDs_51[i].uProjectCode;

                return g_PartNumIDs_51[i].szPartNumber;
            }
        }
    }

    if (puProjectCode != NULL)
        *puProjectCode = 0;

    char szBuf[64];
    _snprintf_s(szBuf, sizeof(szBuf), _TRUNCATE, "[%08x]", uID);
    return szBuf;
}

