    uLDROM_Size = (0x07 - uLDSEL) * 1024;

    if (uLDROM_Size > 4096) {
        uLDROM_Size = 4096;
    }

    uNVM_Size = 0;

    if (uFlashType & 0x0F) {
        uNVM_Size = 0x2800 - uLDROM_Size;
    }

    *puLDROM_Addr = uProgramMemorySize - uLDROM_Size;
    *puLDROM_Size = uLDROM_Size;
    *puAPROM_Size = uProgramMemorySize - uLDROM_Size - uNVM_Size;
    *puNVM_Size   = uNVM_Size;
